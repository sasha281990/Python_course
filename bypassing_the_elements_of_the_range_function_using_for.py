# Мишка и игра
# Входные данные
# В первой строке входных данных содержится число n  — количество раундов игры.
# Следующие n строк содержат описание раундов. В i-й из них содержится пара целых чисел mi и ci — результаты бросков
# Мишки и Криса в i-ом раунде соответственно.
# Выходные данные
# В случае победы Мишки в единственной строке выведите "Mishka" (без кавычек), а в случае победы Криса выведите "Chris"
# (без кавычек). Если же игра сведётся к ничьей, то выведите "Friendship is magic!^^" (без кавычек).
n = int(input())
mishka_wins = 0
chris_wins = 0

for _ in range(n):
    mishka, chris = map(int, input().split())
    if mishka > chris:
        mishka_wins += 1
    elif chris > mishka:
        chris_wins += 1

if mishka_wins > chris_wins:
    print("Mishka")
elif chris_wins > mishka_wins:
    print("Chris")
else:
    print("Friendship is magic!^^")

# Найдите, в каких строках из введённых и в каком месте упоминается "рок", причем регистр букв не важен.
# Вместо явного цикла прохода по строке в цикле используйте подходящий метод строки.
